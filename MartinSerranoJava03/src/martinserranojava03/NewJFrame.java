    private void SiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SiguienteActionPerformed
        // TODO add your handling code here:
        if (!EnCreacion) { //preguntamos por la bandera
            
            //si la bandera no es nula//
            if (Cuentas.getActual().getSiguiente() != null) {

                Cuentas.setActual(Cuentas.getActual().getSiguiente()); //configuramos cuenta//
                actual = (Cuenta) Cuentas.getActual().getDato(); //Cogemos los datos//

                //Recoger texto//
                
                numeroCuenta.setText("" + actual.getNumCuen()); //numero de cuenta//
                propietario.setText("" + actual.getPropi()); //nombre propietario//
                fechaApertura.setText("" + actual.getFechap()); //fecha apertura//
                sueldo.setText("" + actual.getSueld()); //configuramos el texto mostrado//

                //habilitar o desabilitar boton//
                
                if(Cuentas.getActual().getSiguiente()==null)
                    Siguiente.setEnabled(false); //configuramos la visibilidad de siguiente//
                if(Cuentas.getActual().getAnterior()!=null)
                    Atras.setEnabled(true); //configuramos la visibilidad de atras//
            }
        } else { //si no es nulo//
            
            int numero = Integer.parseInt(numeroCuenta.getText()); //cojemos el numero introducido//
            int saldo = Integer.parseInt(sueldo.getText()); //el saldo introducido//
            Cuentas.insertarNodo(numero, saldo, propietario.getText()); //insertamos el nodo//
            
            //luego usaremos de nuevo la funcion para terminar de crear//
             
            terminarCrear(Siguiente, Atras, Crear);
        }
    }//GEN-LAST:event_SiguienteActionPerformed

    private void CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearActionPerformed
      
        //Habilitamos los campos//
        
        sueldo.setEditable(true); //ponemos en true el sueldo//
        propietario.setEditable(true); //igual con el propietario//
        
        //Creamos una nueva fecha//
        
        GregorianCalendar fecha = new GregorianCalendar();
        
        //Ahora vaciamos los campos//
        
        numeroCuenta.setText(""+ numCuen); //numero de cuenta automatico//
        propietario.setText(""); //propietario//
        sueldo.setText(""); //sueldo//
     
        //Creamos la fecha de apertura//
            
        fechaApertura.setText(""+fecha.get(Calendar.DAY_OF_MONTH) + "/" //para el dia//
                + (fecha.get(Calendar.MONTH) + 1) + "/" //para el mes//
                + fecha.get(Calendar.YEAR)); //para el año//
        
        //Ahora cambiamos el texto de los botones//
        
        Atras.setText("Cancelar"); //atras sera cancelar//
        Siguiente.setText("Aceptar"); //siguiente sera aceptar//
        
        //Configuramos la visibilidad de los botones//
        
        Crear.setEnabled(false); //crear lo ponemos en falso//
        Siguiente.setEnabled(true); //siguiente lo mostramos para que sea aceptar//
        Atras.setEnabled(true); //lo mismo con atras,sera cancelar//
        
        //bajamos la bandera para indicar que hemos terminado de crear//
        
        EnCreacion = true;
    }//GEN-LAST:event_CrearActionPerformed

    private void terminarCrear(JButton Siguiente, JButton Atras, JButton Crear) {
        
        //configuramos el texto de los botones//
        
        Atras.setText("Atrás"); //boton atras//
        Siguiente.setText("Siguiente"); //boton siguiente//
        
        //Ponemos activo el boton crear y falso el de siguiente//
        
        Crear.setEnabled(true);  //boton crear en verdadero//
        Siguiente.setEnabled(false); //boton siguiente en falso//
        
        //Configuramos que el actual sea el final//
        
        Cuentas.setActual(Cuentas.getFin()); //guardamos en actual la cuenta final//
        actual = (Cuenta) Cuentas.getActual().getDato(); //casteamos en cuentas//

        //configuramos que no se pueda escribir en el camp//
        
        propietario.setEditable(false); //propietario//
        fechaApertura.setEditable(false); //fecha de apertura//
        numeroCuenta.setEditable(false); //numero de cuenta//
        sueldo.setEditable(false); //sueldo//


        EnCreacion = false; //Ponemos la bandera en falso//

        //Configuramos los campos de texto para que nos saque los datos//
        
        propietario.setText(""+actual.getPropi()); //propietario//
        fechaApertura.setText(""+actual.getFechap()); //fecha de apertura//
        numeroCuenta.setText(""+actual.getNumCuen()); //numero de cuenta//
        sueldo.setText(""+actual.getSueld()); //sueldo//

    }
    
