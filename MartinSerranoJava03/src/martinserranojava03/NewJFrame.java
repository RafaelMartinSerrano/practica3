    private void AtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtrasActionPerformed
        // TODO add your handling code here:
        if (!EnCreacion) { //preguntamos por la bandera//
            if (Cuentas.getActual().getAnterior() != null) { //luego por actual y anterior//

                Cuentas.setActual(Cuentas.getActual().getAnterior()); //asignamos la cuenta a la actual//
                actual = (Cuenta) Cuentas.getActual().getDato(); //cojemos los datos//

                //Recoger texto//
                numeroCuenta.setText("" + actual.getNumCuen());
                propietario.setText("" + actual.getPropi());
                fechaApertura.setText("" + actual.getFechap());
                sueldo.setText("" + actual.getSueld());

                //habilitar o deshabilitar boton//
                if(Cuentas.getActual().getSiguiente()!=null)
                    Siguiente.setEnabled(true); //si no es nulo lo activamos
                if(Cuentas.getActual().getAnterior()==null)
                    Atras.setEnabled(false); //si lo deshabilitamos el de atras//
            }
        } else {
            terminarCrear(Siguiente, Atras, Crear); //si no terminamos lafuncion//
        }
    }//GEN-LAST:event_AtrasActionPerformed

